-- ------------------------------------------------------------------------------------------------------------------------------
-- BUILD DATABASE SCHEMA --------------------------------------------------------------------------------------------------------
-- ------------------------------------------------------------------------------------------------------------------------------

-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Schema: grading_system
-- ------------------------------------------------------------------------------------------------------------------------------
DROP SCHEMA IF EXISTS `grading_system` ;
CREATE SCHEMA `grading_system` DEFAULT CHARACTER SET utf8 ;
USE grading_system;

-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `grading_system`.`course_catalog`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `grading_system`.`course_catalog` ;
CREATE TABLE `course_catalog` (
  `catalog_id` int NOT NULL AUTO_INCREMENT,
  `dept_abbrv` varchar(10) DEFAULT NULL,
  `course_number` int DEFAULT NULL,
  `course_name` varchar(128) DEFAULT NULL,
  `department_id` int DEFAULT NULL,
  PRIMARY KEY (`catalog_id`),
  KEY `catalog_department_id_idx` (`department_id`),
  CONSTRAINT `catalog_department_id` FOREIGN KEY (`department_id`) REFERENCES `departments` (`department_id`)
) ENGINE=InnoDB AUTO_INCREMENT=224 DEFAULT CHARSET=utf8;


-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `grading_system`.`semesters`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `grading_system`.`semesters` ;
CREATE TABLE `semesters` (
  `semester_id` int NOT NULL AUTO_INCREMENT,
  `semester` varchar(4) DEFAULT NULL,
  PRIMARY KEY (`semester_id`)
) ENGINE=InnoDB AUTO_INCREMENT=14 DEFAULT CHARSET=utf8;

-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `grading_system`.`departments`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `grading_system`.`departments` ;
CREATE TABLE `departments` (
  `department_id` int NOT NULL AUTO_INCREMENT,
  `department_name` varchar(128) DEFAULT NULL,
  PRIMARY KEY (`department_id`)
) ENGINE=InnoDB AUTO_INCREMENT=28 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `grading_system`.`faculty`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `grading_system`.`faculty` ;
CREATE TABLE `faculty` (
  `faculty_id` int NOT NULL AUTO_INCREMENT,
  `school_id` varchar(45) DEFAULT NULL,
  `first_name` varchar(45) DEFAULT NULL,
  `last_name` varchar(45) DEFAULT NULL,
  `department_id` int DEFAULT NULL,
  PRIMARY KEY (`faculty_id`),
  KEY `department_id_idx` (`department_id`),
  CONSTRAINT `fac_department_id` FOREIGN KEY (`department_id`) REFERENCES `departments` (`department_id`)
) ENGINE=InnoDB AUTO_INCREMENT=101 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `grading_system`.`majors`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `grading_system`.`majors` ;
CREATE TABLE `majors` (
  `major_id` int NOT NULL AUTO_INCREMENT,
  `department_id` int DEFAULT NULL,
  `major_name` varchar(128) DEFAULT NULL,
  PRIMARY KEY (`major_id`),
  KEY `major_department_id_idx` (`department_id`),
  CONSTRAINT `major_department_id` FOREIGN KEY (`department_id`) REFERENCES `departments` (`department_id`)
) ENGINE=InnoDB AUTO_INCREMENT=28 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `grading_system`.`students`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `grading_system`.`students` ;
CREATE TABLE `students` (
  `student_id` int NOT NULL AUTO_INCREMENT,
  `first_name` varchar(45) DEFAULT NULL,
  `last_name` varchar(45) DEFAULT NULL,
  `class_standing` varchar(20) DEFAULT NULL,
  `school_id` varchar(45) DEFAULT NULL,
  `cumulative_gpa` decimal(6,2) DEFAULT NULL,
  `major_id` int DEFAULT NULL,
  PRIMARY KEY (`student_id`),
  KEY `student_major_id_idx` (`major_id`),
  CONSTRAINT `student_major_id` FOREIGN KEY (`major_id`) REFERENCES `majors` (`major_id`)
) ENGINE=InnoDB AUTO_INCREMENT=405 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `grading_system`.`classes`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `grading_system`.`classes` ;
CREATE TABLE `classes` (
  `class_id` int NOT NULL AUTO_INCREMENT,
  `catalog_id` int DEFAULT NULL,
  `semester_id` int DEFAULT NULL,
  `section` int DEFAULT NULL,
  `faculty_id` int DEFAULT NULL,
  PRIMARY KEY (`class_id`),
  KEY `faculty_id_idx` (`faculty_id`),
  KEY `class_semester_id_idx` (`semester_id`),
  KEY `class_catalog_id_idx` (`catalog_id`),
  CONSTRAINT `class_catalog_id` FOREIGN KEY (`catalog_id`) REFERENCES `course_catalog` (`catalog_id`),
  CONSTRAINT `class_faculty_id` FOREIGN KEY (`faculty_id`) REFERENCES `faculty` (`faculty_id`),
  CONSTRAINT `class_semester_id` FOREIGN KEY (`semester_id`) REFERENCES `semesters` (`semester_id`)
) ENGINE=InnoDB AUTO_INCREMENT=34 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `grading_system`.`registration`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `grading_system`.`registration` ;
CREATE TABLE `registration` (
  `registration_id` int NOT NULL AUTO_INCREMENT,
  `semester_id` int DEFAULT NULL,
  `class_id` int DEFAULT NULL,
  `student_id` int DEFAULT NULL,
  `grade` varchar(2) DEFAULT NULL,
  PRIMARY KEY (`registration_id`),
  KEY `registration_semester_id_idx` (`semester_id`),
  KEY `registration_class_id_idx` (`class_id`),
  KEY `registration_student_id_idx` (`student_id`),
  CONSTRAINT `registration_class_id` FOREIGN KEY (`class_id`) REFERENCES `classes` (`class_id`),
  CONSTRAINT `registration_semester_id` FOREIGN KEY (`semester_id`) REFERENCES `semesters` (`semester_id`),
  CONSTRAINT `registration_student_id` FOREIGN KEY (`student_id`) REFERENCES `students` (`student_id`)
) ENGINE=InnoDB AUTO_INCREMENT=254 DEFAULT CHARSET=utf8;

