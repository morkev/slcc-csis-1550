-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `record_store`.`customers`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `record_store`.`customers` ;
CREATE TABLE `customers` 
	(
	  `customer_id` int NOT NULL AUTO_INCREMENT,
	  `first_name` varchar(45) DEFAULT NULL,
	  `last_name` varchar(45) DEFAULT NULL,
	  `address1` varchar(128) DEFAULT NULL,
	  `city` varchar(128) DEFAULT NULL,
	  `state` varchar(2) DEFAULT NULL,
	  `zip` varchar(10) DEFAULT NULL,
	  `country` varchar(2) DEFAULT NULL,
	  `phone` varchar(12) DEFAULT NULL,
	  `email` varchar(128) DEFAULT NULL,
	  `twitter` varchar(45) DEFAULT NULL,
	  PRIMARY KEY (`customer_id`)
	) ENGINE=InnoDB AUTO_INCREMENT=6 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `record_store`.`orders`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `record_store`.`orders` ;
	CREATE TABLE `orders` 
	(
	  `order_id` int NOT NULL AUTO_INCREMENT,
	  `customer_id` int DEFAULT NULL,
	  `order_date` date DEFAULT NULL,
	  `order_status` int DEFAULT NULL,
	  `shipped` date DEFAULT NULL,
	  `delivered` date DEFAULT NULL,
	  PRIMARY KEY (`order_id`),
	  KEY `customer_id_idx` (`customer_id`),
	  KEY `order_status_idx` (`order_status`),
	  CONSTRAINT `customer_id` FOREIGN KEY (`customer_id`) REFERENCES `customers` (`customer_id`),
	  CONSTRAINT `order_status` FOREIGN KEY (`order_status`) REFERENCES `lookup_order_status` (`lkp_status_id`)
	) ENGINE=InnoDB AUTO_INCREMENT=310 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;


-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `record_store`.`order_items`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `record_store`.`order_items` ;
CREATE TABLE `order_items` 
	(
	  `order_item_id` int NOT NULL AUTO_INCREMENT,
	  `order_id` int DEFAULT NULL,
	  `product_id` int DEFAULT NULL,
	  `price` decimal(8,2) DEFAULT NULL,
	  PRIMARY KEY (`order_item_id`),
	  KEY `order_id_idx` (`order_id`),
	  KEY `product_id_idx` (`product_id`),
	  CONSTRAINT `order_id` FOREIGN KEY (`order_id`) REFERENCES `orders` (`order_id`),
	  CONSTRAINT `product_id` FOREIGN KEY (`product_id`) REFERENCES `products` (`product_id`)
	) ENGINE=InnoDB AUTO_INCREMENT=19 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `record_store`.`products`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `record_store`.`products` ;
CREATE TABLE `products` 
	(
	  `product_id` int NOT NULL AUTO_INCREMENT,
	  `artist` varchar(45) DEFAULT NULL,
	  `title` varchar(128) DEFAULT NULL,
	  `released` int DEFAULT NULL,
	  `price` decimal(8,2) DEFAULT NULL,
	  `media` varchar(45) DEFAULT NULL,
	  `stock` int DEFAULT NULL,
	  PRIMARY KEY (`product_id`)
	) ENGINE=InnoDB AUTO_INCREMENT=14 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

-- ------------------------------------------------------------------------------------------------------------------------------
-- Build Table `record_store`.`products`
-- ------------------------------------------------------------------------------------------------------------------------------
DROP TABLE IF EXISTS `record_store`.`lookup_order_status` ;
CREATE TABLE `lookup_order_status` 
