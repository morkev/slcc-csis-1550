	  `lkp_status_id` int NOT NULL AUTO_INCREMENT,
	  `status` varchar(45) DEFAULT NULL,
	  PRIMARY KEY (`lkp_status_id`)
	) ENGINE=InnoDB DEFAULT CHARSET=utf8;

